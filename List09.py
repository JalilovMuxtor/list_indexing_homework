def main(list1):
    """
    A list of several elements is given. True if all items are the same, otherwise return False.
    Args:
        list1 (list): parameter
    Returns:
        bool: return answer
    """
    b=list1
    i=0
    while i<len(b):
        return True
    else:
        return False
print(main([0,0,0,0,0]))